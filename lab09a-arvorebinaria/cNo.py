# Classe nó de uma Árvore Binária
# ##################################################

class cNo:

# *******************************************************
# *******************************************************
  def __init__(self, dado=0, pai=None):
    self.__pai__    = pai
    self.__dado__   = dado
    self.__fEsq__   = None
    self.__fDir__   = None
  
# *******************************************************
  def getDado(self):
    return self.__dado__
  
# *******************************************************
  def setDado(self, valor):
    self.__dado__ = valor
  
# *******************************************************
  def getFilhoEsq(self):
    return self.__fEsq__
  
# *******************************************************
  def setFilhoEsq(self, filhoEsq):
    self.__fEsq__ = filhoEsq
  
# *******************************************************
  def getFilhoDir(self):
    return self.__fDir__
  
# *******************************************************
  def setFilhoDir(self, filhoDir):
    self.__fDir__ = filhoDir

# *******************************************************
  def getPai(self):
    return self.__pai__

# *******************************************************
  def setPai(self, pai):
    self.__pai__ = pai

# *******************************************************
# *******************************************************
    def __str__(self):

        outStr = ""
        outStr +=  "+=================++======+=================+\n"
        outStr += f' | {id(self.__fEsq__)} | {self.__dado__:4} | {id(self.__fDir__)} |\n'
        outStr +=  "+=================++======+=================+\n"
        return outStr

